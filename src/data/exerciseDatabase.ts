export function getAllCategories(): ExerciseCategory[] {
  return EXERCISE_CATEGORIES;
}

export function getCategoryById(id: string): ExerciseCategory | undefined {
  return EXERCISE_CATEGORIES.find((c) => c.id === id);
}

export function getExercisesByCategory(categoryId: string): string[] {
  const cat = getCategoryById(categoryId);
  if (!cat) return [];
  return [...cat.exercises, ...cat.alternatives];
}

export function getAllExercisesFlat(): string[] {
  const all = new Set<string>();
  EXERCISE_CATEGORIES.forEach((cat) => {
    cat.exercises.forEach((e) => all.add(e));
    cat.alternatives.forEach((e) => all.add(e));
  });
  return Array.from(all).sort();
}

export function findCategoryForExercise(exerciseName: string): string | null {
  for (const cat of EXERCISE_CATEGORIES) {
    if (cat.exercises.includes(exerciseName) || cat.alternatives.includes(exerciseName)) {
      return cat.id;
    }
  }
  return null;
}

export function getExerciseAlternatives(exerciseName: string): string[] {
  const catId = findCategoryForExercise(exerciseName);
  if (!catId) return [];
  const cat = getCategoryById(catId);
  if (!cat) return [];
  return cat.alternatives.filter((e) => e !== exerciseName);
}

// ═══════════════════════════════════════════════════════════════
// PROGRAM TEMPLATE BUILDER
// When you select a category slot (A1, A2, B1, etc.),
// this gives you the exercise pool for that movement pattern
// ═══════════════════════════════════════════════════════════════

export interface ProgramSlot {
  order: string;        // A1, A2, B1, B2, C1, C2, C3, D
  categoryId: string;   // which movement pattern
  exercise: string;     // selected exercise
  sets: number;
  reps: string;
  tempo: string;
  restSeconds: number;
}

export function getSlotCategory(slotOrder: string): string {
  const slotMap: Record<string, string> = {
    A1: 'pulling',
    A2: 'unilateral_quad',
    B1: 'pressing',
    B2: 'posterior',
    C1: 'delt_scap',
    C2: 'biceps',
    C3: 'bracing',
    D: 'metcon_bracing',
  };
  return slotMap[slotOrder] || 'target_areas';
}

export function getDefaultSlotExercise(slotOrder: string): string {
  const defaults: Record<string, string> = {
    A1: 'Chin up - Semi supinated',
    A2: 'DB Split Squat',
    B1: '15° DB Incline Press',
    B2: 'Seated Leg Curl',
    C1: 'DB Lateral Raise - Seated',
    C2: '80° DB Incline Curl',
    C3: 'Front Plank',
    D: 'Farmers Walk',
  };
  return defaults[slotOrder] || '';
}
